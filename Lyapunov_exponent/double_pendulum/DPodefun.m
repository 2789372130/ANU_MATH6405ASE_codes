function dydt = DPodefun(t,y,m1,m2,l1,l2,g)
t1 = y(1);
t2 = y(2);
p1 = y(3);
p2 = y(4);

dt1 = (l2*p1-l1*p2 * cos(t1-t2))/(l1^2*l2*(m1+m2*(sin(t1-t2))^2));
dt2 = (l1*(m1+m2)*p2 - l2*m2*p1*cos(t1-t2))/(l1*l2^2*m2*(m1+m2*(sin(t1-t2)^2)));
C1 = (p1*p2*sin(t1-t2))/(l1*l2*(m1+m2*(sin(t1-t2))^2));
C2 = (l2^2*m2*p1^2+l1^2*(m1+m2)*p2^2-2*l1*l2*m2*p1*p2*cos(t1-t2))*sin(2*(t1-t2))/(2*l1^2*l2^2*((m1+m2*(sin(t1-t2))^2))^2);
dp1 = -(m1+m2)*g*l1*sin(t1) - C1 + C2;
dp2 = -m2*g*l2*sin(t2) + C1 - C2;

M = zeros([4,4]);

M(1,1) = ((-4*l2*m2*p1*cos(t1-t2) + l1*p2*(2*m1 + 3*m2 + m2*cos(2*(t1 - t2)))) * sin(t1 - t2))/(2*l1^2*l2*(m1 + m2*sin(t1 - t2)^2)^2);
M(1,2) = -((-4*l2*m2*p1*cos(t1 - t2) + l1*p2* (2* m1 + 3 *m2 + m2* cos(2 *(t1 - t2))))* sin(t1 - t2))/(2*l1^2 *l2* (m1 + m2 *sin(t1 - t2)^2)^2);
M(1,3) = 1/(l1^2 *(m1 + m2 *sin(t1 - t2)^2));
M(1,4) = -(cos(t1 - t2)/(l1* l2 *(m1 + m2 *sin(t1 - t2)^2)));

M(2,1) = ((-4 *l1 *(m1 + m2)* p2 *(cos(t1 - t2)) + l2* p1* (2 *m1 + 3* m2 + m2* cos(2 *(t1 - t2)))) *(sin(t1 - t2)))/(2* l1* l2^2 *(m1 + m2* (sin(t1 - t2))^2)^2);
M(2,2) = -(((-4 *l1 *(m1 + m2) *p2* (cos(t1 - t2)) + l2* p1* (2 *m1 + 3* m2 + m2* cos(2* (t1 - t2)))) *(sin(t1 - t2)))/(2 *l1* l2^2 *(m1 + m2 *(sin(t1 - t2))^2)^2));
M(2,3) = -((cos(t1 - t2))/(l1* l2 *(m1 + m2* (sin(t1 - t2))^2)));
M(2,4) = (m1 + m2)/(l2^2* m2 *(m1 + m2 *(sin(t1 - t2))^2));

M(3,1) = (1/l1^2)*(-(g*l1^3*(m1 + m2)*(cos(t1))) + ((cos(t1 - t2))*(l2*p1 - l1*p2*(cos(t1 - t2)))* ...
     (l2*m2*p1*(cos(t1 - t2)) - l1*p2*(m1 + m2)))/(l2^2*(m1 + m2*(sin(t1 - t2))^2)^2) + ...
   (l1*p2*(sin(t1 - t2))^2*(l2*m2*p1*(cos(t1 - t2)) - l1*p2*(m1 + m2)))/ ...
    (l2^2*(m1 + m2*(sin(t1 - t2))^2)^2) + (4*m2*(sin(t1 - t2))^2*(cos(t1 - t2))* ...
     (l1*p2*(cos(t1 - t2)) - l2*p1)*(l2*m2*p1*(cos(t1 - t2)) - l1*p2*(m1 + m2)))/ ...
    (l2^2*(m1 + m2*(sin(t1 - t2))^2)^3) + ...
   (m2*p1*(sin(t1 - t2))^2*(l1*p2*(cos(t1 - t2)) - l2*p1))/(l2*(m1 + m2*(sin(t1 - t2))^2)^2));
M(3,2) = (2*l1*l2*(8*m1^2 + 4*m1*m2 - 11*m2^2)*p1*p2*(cos(t1 - t2)) - ...
   8*(2*m1 + m2)*(l2^2*m2*p1^2 + l1^2*(m1 + m2)*p2^2)*(cos(2*(t1 - t2))) + ...
   m2*(12*l2^2*m2*p1^2 + 12*l1^2*m1*p2^2 + 12*l1^2*m2*p2^2 + ...
     3*l1*l2*(8*m1 + 7*m2)*p1*p2*(cos(3*(t1 - t2))) - ...
     4*(l2^2*m2*p1^2 + l1^2*(m1 + m2)*p2^2)*(cos(4*(t1 - t2))) + ...
     l1*l2*m2*p1*p2*(cos(5*(t1 - t2)))))/(16*l1^2*l2^2*(m1 + m2*(sin(t1 - t2))^2)^3);
M(3,3) = -(((l1*(m1 + m2)*p2 - 2*l2*m2*p1*(cos(t1 - t2)) + l1*m2*p2*(cos(t1 - t2))^2)*(sin(t1 - t2)))/ ...
   (l1^2*l2*(m1 + m2*(sin(t1 - t2))^2)^2));
M(3,4) = -(((l1*(m1 + m2)*p2 - 2*l2*m2*p1*(cos(t1 - t2)) + l1*m2*p2*(cos(t1 - t2))^2)*(sin(t1 - t2)))/ ...
   (l1^2*l2*(m1 + m2*(sin(t1 - t2))^2)^2));

M(4,1) = (2*l1*l2*(8*m1^2 + 4*m1*m2 - 11*m2^2)*p1*p2*(cos(t1 - t2)) - ...
   8*(2*m1 + m2)*(l2^2*m2*p1^2 + l1^2*(m1 + m2)*p2^2)*(cos(2*(t1 - t2))) +... 
   m2*(12*l2^2*m2*p1^2 + 12*l1^2*m1*p2^2 + 12*l1^2*m2*p2^2 + ...
     3*l1*l2*(8*m1 + 7*m2)*p1*p2*(cos(3*(t1 - t2))) - ...
     4*(l2^2*m2*p1^2 + l1^2*(m1 + m2)*p2^2)*(cos(4*(t1 - t2))) + ... 
     l1*l2*m2*p1*p2*(cos(5*(t1 - t2)))))/(16*l1^2*l2^2*(m1 + m2*(sin(t1 - t2))^2)^3);
M(4,2) = (m1*(l2^2*m2*p1^2 + l1^2*(m1 + m2)*p2^2)*(cos(t1 - t2))^2 - ...
   l1*l2*m2*p1*p2*(cos(t1 - t2))^3*(2*m1 - 3*m2 + 3*m2*(cos(2*(t1 - t2)))) - ... 
   (1/8)*g*l1^2*l2^3*m2*(2*m1 + m2 - m2*(cos(2*(t1 - t2))))^3*(cos(t2)) - ...
   (l2^2*m2*p1^2 + l1^2*(m1 + m2)*p2^2)*(m1 + 2*m2 + m2*(cos(2*(t1 - t2))))*...
    (sin(t1 - t2))^2 + l1*l2*p1*p2*(cos(t1 - t2))*(-m1^2 + 4*m1*m2*(sin(t1 - t2))^2 +... 
     5*m2^2*(sin(t1 - t2))^4))/(l1^2*l2^2*(m1 + m2*(sin(t1 - t2))^2)^3);
M(4,3) = ((-4*l2*m2*p1*(cos(t1 - t2)) + l1*p2*(2*m1 + 3*m2 + m2*(cos(2*(t1 - t2)))))*(sin(t1 - t2)))/...
  (2*l1^2*l2*(m1 + m2*(sin(t1 - t2))^2)^2);
M(4,4) = ((-4*l2*m2*p1*(cos(t1 - t2)) + l1*p2*(2*m1 + 3*m2 + m2*(cos(2*(t1 - t2)))))*(sin(t1 - t2)))/...
  (2*l1^2*l2*(m1 + m2*(sin(t1 - t2))^2)^2);

dy = M * y(5:8);

dydt = [dt1;dt2;dp1;dp2;dy(1);dy(2);dy(3);dy(4)];
end
